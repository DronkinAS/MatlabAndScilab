function K = con_K(r,v,h)

K = [];

k1 = [];
a = fdv(v,fE(r),fB(r));
k1(1) = a(1); k1(2) = a(2); k1(3) = a(3);
k1(4) = v(1); k1(5) = v(2); k1(6) = v(3);

v_n = [];
v_n(1) = v(1) + (h/2)*k1(1);
v_n(2) = v(2) + (h/2)*k1(2);
v_n(3) = v(3) + (h/2)*k1(3);
r_n = [];
r_n(1) = r(1) + (h/2)*k1(4);
r_n(2) = r(2) + (h/2)*k1(5);
r_n(3) = r(3) + (h/2)*k1(6);

k2 = [];
a = fdv(v_n,fE(r_n),fB(r_n));
k2(1) = a(1); k2(2) = a(2); k2(3) = a(3);
k2(4) = v_n(1); k2(5) = v_n(2); k2(6) = v_n(3);

v_n(1) = v(1) + (h/2)*k2(1);
v_n(2) = v(2) + (h/2)*k2(2);
v_n(3) = v(3) + (h/2)*k2(3);
r_n(1) = r(1) + (h/2)*k2(4);
r_n(2) = r(2) + (h/2)*k2(5);
r_n(3) = r(3) + (h/2)*k2(6);

k3 = [];
a = fdv(v_n,fE(r_n),fB(r_n));
k3(1) = a(1); k3(2) = a(2); k3(3) = a(3);
k3(4) = v_n(1); k3(5) = v_n(2); k3(6) = v_n(3);

v_n(1) = v(1) + h*k3(1);
v_n(2) = v(2) + h*k3(2);
v_n(3) = v(3) + h*k3(3);
r_n(1) = r(1) + h*k3(4);
r_n(2) = r(2) + h*k3(5);
r_n(3) = r(3) + h*k3(6);

k4 = [];
a = fdv(v_n,fE(r_n),fB(r_n));
k4(1) = a(1); k4(2) = a(2); k4(3) = a(3);
k4(4) = v_n(1); k4(5) = v_n(2); k4(6) = v_n(3);

K = [k1; k2; k3; k4];

